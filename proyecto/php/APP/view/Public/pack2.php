        <h1>Contenido del paquete 2 "Comercialización electrónico"</h1>
        <p>Una página web de comercio electrónico, diseñada para la venta de productos o servicios en línea, 
        debe contar con una serie de módulos y características para proporcionar una experiencia de compra 
